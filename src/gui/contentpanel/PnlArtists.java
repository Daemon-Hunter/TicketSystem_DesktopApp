    private void txtSearchbarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchbarKeyPressed
        
        if (txtSearchbar.getText().contains("Search Artists...")) {
            txtSearchbar.setText("");
        }
        if(txtSearchbar.getText().contains("Stormzy") || txtSearchbar.getText().contains("stormzy"))
        {
            pnlArtist2.setVisible(false);
            pnlArtist3.setVisible(false);
            pnlArtist4.setVisible(false);
            pnlArtist5.setVisible(false);
            lblArtistName1.setText("Stormzy");
            imgArtist1.setIcon(new ImageIcon("src/images/Artists/stormzy.jpeg"));
        }
        else
        {
            pnlArtist2.setVisible(true);
            pnlArtist3.setVisible(true);
            pnlArtist4.setVisible(true);
            pnlArtist5.setVisible(true);
            initArtists();

        }
            
        
        
    }//GEN-LAST:event_txtSearchbarKeyPressed

